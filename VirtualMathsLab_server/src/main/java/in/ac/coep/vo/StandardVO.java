package in.ac.coep.vo;

/**
 * @author Prashant
 *
 */
public class StandardVO {

	private int stdId;

	private String name;


	/**
	 * @return the stdId
	 */
	public int getStdId() {
		return stdId;
	}

	/**
	 * @param stdId the stdId to set
	 */
	public void setStdId(int stdId) {
		this.stdId = stdId;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
